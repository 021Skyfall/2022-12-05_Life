public class Life_datatype {
    public static void main(String[] args) {
        System.out.println(8+8); //Number
        System.out.println("ARA"); //String
        System.out.println("54"); //String
        System.out.println(54*87*76+84+21-500000); //Number Calculate
        System.out.println("54"+"String?"); //String + String
        System.out.println("UCLA"+88); //String + Number
        System.out.println("1111".length()); //String or Number length
        System.out.println("SSSSSSSSSSSSSSSSSS".length());
    }
}
