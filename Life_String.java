public class Life_String {
    public static void main(String[] args) {
        System.out.println("Hello World"); // String
        System.out.println('H'); // Character -> 자바에서는 문자 단 하나를 나타냄
        System.out.println("H"); // String
        System.out.println("Hello "
                + "World"); // 이건 그냥 String + String
        System.out.println("Hello \nWorld"); // \n = New line 기호쓸때는 \ 역슬래시
        System.out.println("Hello \"World\""); // Hello "World" = Escape
    }
}
